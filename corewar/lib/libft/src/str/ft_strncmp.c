#include <string.h>

int		ft_strncmp(char *s1, char *s2, size_t n)
{
	if (!n)
		return (0);
	while (*s1 == *s2 && *s1 != '\0' && *s2 != '\0' && n > 1)
	{
		++s1;
		++s2;
		n--;
	}
	return (*s1 - *s2);
}
